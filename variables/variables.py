nombre = "Emanuel"

bienvenida = "Hola " + nombre + " como estas?"

# Borramos la variable bienvenida

del bienvenida

# Declaramos bienvenida 2 con SNAKE CASE

bienvenida_2 = "Hola"

# Concatenar con +

bienvenida_2 = "Hola" + ", como estas?"

# Concatenar con f strings

bienvenida_2 = f"Hola {nombre} como estas?"

# Operadores de pertenencia (in & not in)

print("ola" in bienvenida_2)  # True

print("Pedro" not in bienvenida_2)  # False
