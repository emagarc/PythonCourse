# Suma y Resta (+&-)

suma = 12 + 5
resta = suma - 8

# Multiplicacion y Division (* & /)

multi = 12 * 5
divi = multi / 2    # la division nos devuelve un dato float (flotante)

# Potenciacion (exponente) (**)

potencia = 12 ** 5
print(potencia)

# Division baja (//)

division_baja = 12 // 5

# devuelve un entero, nunca decimales por ende es un tipo de dato INT
tipo_de_dato = type(division_baja)
print(tipo_de_dato)

# Modulo o Resto (%)

resto = 12 % 5
